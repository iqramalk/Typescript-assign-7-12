// This TypeScript program demonstrates basic string manipulation functions like toUpperCase(),
// toLowerCase(), and replace().

const message = "Hello, Everyone I am a Content writer.";

// Convert the string to uppercase
const upperCaseMessage = message.toUpperCase();
console.log(`Uppercase message: ${upperCaseMessage}`);

// Convert the string to lowercase
const lowerCaseMessage = message.toLowerCase();
console.log(`Lowercase message: ${lowerCaseMessage}`);

