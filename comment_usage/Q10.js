// This TypeScript program demonstrates basic string manipulation functions like toUpperCase(),
// toLowerCase(), and replace().
var message = "Hello, Everyone I am a Content writer.";
// Convert the string to uppercase
var upperCaseMessage = message.toUpperCase();
console.log("Uppercase message: ".concat(upperCaseMessage));
// Convert the string to lowercase
var lowerCaseMessage = message.toLowerCase();
console.log("Lowercase message: ".concat(lowerCaseMessage));
